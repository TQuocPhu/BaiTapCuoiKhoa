// Branch
class Branch {
    id_branch;
    name_branch;

    constructor(id_branch, name_branch) {
        this.id_branch = id_branch;
        this.name_branch = name_branch;
    }

    getIdBranch() {
        return this.id_branch;
    }
    getNameBranch() {
        return this.name_branch;
    }
}